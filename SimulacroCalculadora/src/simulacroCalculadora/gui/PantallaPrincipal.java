    /**
     * Inicializa la tabla y sus componentes
     */
    private void inicializarTabla() {
        // Inicio dtm
        dtm = new DefaultTableModel() {
            @Override
            public Class getColumnClass(int column) {
                // Columna 3 (índice 3, la cuarta columna: "Resultado")
                if (column == 3) {
                    return Double.class; // Indica que los datos en esta columna son Double
                }
                // Para las otras columnas, mantiene el tipo Object (String por defecto)
                return Object.class;
            }
        };

        String[] titulos = new String[]{"Operación", "Num 1", "Num 2", "Resultado"};
        dtm.setColumnIdentifiers(titulos);
        jTableOperaciones.setModel(dtm);

        // Sorter
        TableRowSorter<TableModel> sorter = new TableRowSorter(jTableOperaciones.getModel());
        jTableOperaciones.setRowSorter(sorter);

        // Orden inicial
        List<RowSorter.SortKey> sortkeys = new ArrayList<>();
        sortkeys.add(new RowSorter.SortKey(3, SortOrder.DESCENDING)); // Columna resultado descendente
        sorter.setSortKeys(sortkeys);

        // Refrescamos tabla
        refrescarDatosTabla();

    }

    // Carga y refresca los datos de la tabla
    private void refrescarDatosTabla() {
        // Borra todas las filas
        dtm.setRowCount(0);

        // Llena el modelo con los datos actualizados
        LogicaOperaciones.getListaOperaciones().forEach(o -> dtm.addRow(new Object[]{o.getNombre(), o.getNum1(), o.getNum2(), o.getResultado()}));

        // Reordenamos los datos
        RowSorter<? extends TableModel> sorter = jTableOperaciones.getRowSorter();
        if (sorter != null) {
            // Usamos el cast al tipo concreto para acceder al método sort()
            ((TableRowSorter<?>) sorter).sort();
        }
    }

