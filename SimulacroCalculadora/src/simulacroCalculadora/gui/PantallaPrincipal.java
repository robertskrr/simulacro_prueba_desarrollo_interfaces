    /**
     * Elimina la operación deseada
     */
    private void eliminarOperacion() {
        String operacionBorrar = JOptionPane.showInputDialog(this, "Introduce la operación a eliminar", "Operación a eliminar", JOptionPane.QUESTION_MESSAGE);
        if (operacionBorrar == null) {
            JOptionPane.showMessageDialog(this, "Eliminación cancelada", "Cancelado", JOptionPane.INFORMATION_MESSAGE);
        } else if (operacionBorrar.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No has introducido nada", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            String resultadoBorrar = JOptionPane.showInputDialog(this, "Introduce el resultado de la operación", "Operación a eliminar", JOptionPane.QUESTION_MESSAGE);
            double resultado = Double.parseDouble(resultadoBorrar);

            Operacion operacion = LogicaOperaciones.getListaOperaciones().stream()
                    .filter(h -> h.getNombre().equalsIgnoreCase(operacionBorrar) && h.getResultado() == resultado)
                    .findFirst().orElse(null);
            if (operacion == null) {
                JOptionPane.showMessageDialog(this, "No hay ninguna operación con esos datos", "ERROR", JOptionPane.WARNING_MESSAGE);
            } else {
                int decisionBorrar = JOptionPane.showConfirmDialog(this, "¿Estás seguro de borrar la operación?", "¿Seguro?", JOptionPane.YES_NO_OPTION);
                if (decisionBorrar == JOptionPane.YES_OPTION) {
                    LogicaOperaciones.getListaOperaciones().remove(operacion);
                    JOptionPane.showMessageDialog(this, "De acuerdo, eliminamos"
                            + " la operación " + operacion.getNombre() + " con resultado " + operacion.getResultado(), "Operación borrada", JOptionPane.INFORMATION_MESSAGE);
                    refrescarDatosTabla();
                } else {
                    JOptionPane.showMessageDialog(this, "Sabía que no querías borrar "
                            + "la operación " + operacion.getNombre() + " con resultado " + operacion.getResultado(), "Operación NO borrada", JOptionPane.INFORMATION_MESSAGE);
                }

            }
        }
    }
